#include <iostream>
using namespace std;

int main()
{
    //lets take an integer and try to find the size of it.
    int a=10;
    
    cout<<"the size of integer a=10 is: "<<sizeof(a)<<endl;

    //also we can find the size of any data type.
    cout<<"the size of Int is: "<<sizeof(int)<<" bytes"<<endl;



    return 0;
}
