#include <iostream>
using namespace std;

int main()
{
    int num=0;

    num+=10;
    cout<<"num += 10: "<<num<<endl;

    num-=10;
    cout<<"num -= 10: "<<num<<endl;

    num *= 10;
    cout<<"num *= 10: "<<num<<endl;

    num /=10;
    cout<<"num /= 10: "<<num<<endl;
    return 0;
}
